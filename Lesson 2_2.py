# Для списка реализовать обмен значений соседних элементов. Значениями обмениваются элементы с
# индексами 0 и 1, 2 и 3 и т. д. При нечётном количестве элементов последний сохранить на своём месте.
# Для заполнения списка элементов нужно использовать функцию input()

list_enter = (input("Введите несколько слов или чисел, пожалуйста, через пробел:")).split()

for i in range(1, len(list_enter), 2):
    list_enter[i - 1], list_enter[i] = list_enter[i], list_enter[i - 1]

print(list_enter)
